    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double span = 0;
        double height = 0;
        double noOfBeams = 0;
       

        double minSpan = 9.5;
        double maxSpan = 19.5;
        
        double minHeight = 380;
        double maxHeight = 850;
        
        double minNoOfBeams = 30;
        double maxNoOfBeams = 57;
        
                
        //Cost limits
        double minCost = 3798301.01;
        double maxCost = 26159351.52;

        // declaring number of input neurons
        int inputCount = 3;

        // declaring number of output neurons
        int outputCount = 1;

        String trainingSetFileName = "prestressedBeam.txt";

        try {

            span = Double.parseDouble(jTextField1.getText());
            height = Double.parseDouble(jTextField2.getText());
            noOfBeams = Double.parseDouble(jTextField3.getText());

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Please enter valid input");
        }
        span = normalizeInput(minSpan, maxSpan, span);
        height = normalizeInput(maxHeight, minHeight, height);
        noOfBeams = normalizeInput(maxNoOfBeams, minNoOfBeams, noOfBeams);

        ArrayList<Double> data = new ArrayList<Double>();
        data.add(span);
        data.add(height);
        data.add(noOfBeams);

        DataSetRow test = new DataSetRow(data);

        //Create Training Set
        DataSet trainingSet = null;
        try {
            trainingSet = TrainingSetImport.importFromFile(trainingSetFileName, inputCount, outputCount, "\t");

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "File not found");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error reading file");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Bad number format");
        }

        //Create Multilayer Perceptron
        System.out.println("Creating neural network");
        MultiLayerPerceptron neuralNet = new MultiLayerPerceptron(TransferFunctionType.TANH, 3, 5, 1);

        //Set Learning Parameters 
        MomentumBackpropagation learningRule = (MomentumBackpropagation) neuralNet.getLearningRule();

        learningRule.learn(trainingSet, 0.01, 1000);  // maximum error and maximum number of iterations
        learningRule.setLearningRate(0.2);              // learning rate
        learningRule.setMomentum(0.7);                  // momentum

        //Learn the training set
        System.out.println("Training neural network...");
        neuralNet.learn(trainingSet);
        System.out.println("Finish!");

        // Test the perceptron 
        System.out.println("Testing the trained neural network");

        double valueEstimation = deNormalize(neuralNet, test,minCost,maxCost);

        System.out.println("Last Result " + valueEstimation);
        
        NumberFormat nf = NumberFormat.getInstance();
        nf.setMinimumFractionDigits(7);
        String output = " Rs: " + nf.format(valueEstimation);
        jLabel6.setText(output);
    }//GEN-LAST:event_jButton1ActionPerformed

    private double normalizeInput(double min, double max, double input) {
        return (input - min) / (max - min);
    }

    private double deNormalize(NeuralNetwork nnet, DataSetRow test, double min, double max) {
        nnet.setInput(test.getInput());
        nnet.calculate();

        double[] networkOutput = nnet.getOutput();
        double result = networkOutput[0];
        return min + result * (max - min);
    }
